'use client';

export default function ThemedButton({ 
  children, 
  variant = 'primary', 
  size = 'md', 
  className = '', 
  ...props 
}) {
  const baseClasses = 'inline-flex items-center justify-center rounded-lg font-medium transition-all duration-300 focus:outline-none focus:ring-2 focus:ring-offset-2';
  
  const variants = {
    primary: 'theme-button hover:theme-button focus:ring-blue-500',
    secondary: 'theme-card theme-border hover:opacity-80 focus:ring-gray-500',
    outline: 'border-2 theme-border hover:theme-card focus:ring-blue-500',
    ghost: 'hover:theme-card focus:ring-gray-500'
  };
  
  const sizes = {
    sm: 'px-3 py-1.5 text-sm',
    md: 'px-4 py-2 text-base',
    lg: 'px-6 py-3 text-lg'
  };
  
  const variantClasses = variants[variant] || variants.primary;
  const sizeClasses = sizes[size] || sizes.md;
  
  return (
    <button 
      className={`${baseClasses} ${variantClasses} ${sizeClasses} ${className}`}
      style={{
        color: variant === 'primary' ? 'var(--button-text)' : 'var(--foreground)',
        background: variant === 'primary' ? 'var(--button-bg)' : 
                   variant === 'secondary' ? 'var(--card-bg)' : 'transparent',
        borderColor: variant === 'outline' ? 'var(--border-color)' : 'transparent'
      }}
      {...props}
    >
      {children}
    </button>
  );
}