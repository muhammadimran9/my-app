import React, { useState, useEffect, useRef } from 'react';
import { initializeApp } from 'firebase/app';
import { getDatabase, ref, push, onValue, serverTimestamp } from 'firebase/database';
import { useAuth } from '../contexts/AuthContext';
import AuthLogin from './AuthLogin';
import ThemedButton from './ThemedButton';
import ThemedCard from './ThemedCard';

const firebaseConfig = {
  apiKey: "AIzaSyCxtPE4mUZV8dV39rRhVMC-F9CgphLTwUo",
  authDomain: "portfolio-project-14a25.firebaseapp.com",
  databaseURL: "https://portfolio-project-14a25-default-rtdb.firebaseio.com",
  projectId: "portfolio-project-14a25",
  storageBucket: "portfolio-project-14a25.firebasestorage.app",
  messagingSenderId: "826236365085",
  appId: "1:826236365085:web:eea249bf097a52244e59f4"
};

const app = initializeApp(firebaseConfig);
const database = getDatabase(app);

const ChatApp = () => {
  const { user, logout, loading } = useAuth();
  const [messages, setMessages] = useState([]);
  const [newMessage, setNewMessage] = useState('');
  const messagesEndRef = useRef(null);

  useEffect(() => {
    if (!user) return;
    
    const messagesRef = ref(database, 'messages');
    const unsubscribe = onValue(messagesRef, (snapshot) => {
      const data = snapshot.val();
      if (data) {
        const messageList = Object.entries(data).map(([key, value]) => ({
          id: key,
          ...value
        }));
        setMessages(messageList.sort((a, b) => a.timestamp - b.timestamp));
      }
    });

    return () => unsubscribe();
  }, [user]);

  useEffect(() => {
    messagesEndRef.current?.scrollIntoView({ behavior: 'smooth' });
  }, [messages]);

  const sendMessage = async (e) => {
    e.preventDefault();
    if (!newMessage.trim() || !user) return;

    const messagesRef = ref(database, 'messages');
    await push(messagesRef, {
      text: newMessage,
      username: user.name,
      userId: user.uid,
      userPhoto: user.photoURL,
      provider: user.provider,
      timestamp: serverTimestamp()
    });

    setNewMessage('');
  };

  if (loading) {
    return (
      <div className="flex items-center justify-center min-h-screen" style={{ background: 'var(--background)' }}>
        <div className="animate-spin rounded-full h-12 w-12 border-b-2" style={{ borderColor: 'var(--button-bg)' }}></div>
      </div>
    );
  }

  if (!user) {
    return <AuthLogin />;
  }

  return (
    <div className="flex flex-col h-screen" style={{ background: 'var(--background)' }}>
      {/* Header */}
      <div className="theme-card p-4 flex justify-between items-center">
        <div>
          <h1 className="text-xl font-bold" style={{ color: 'var(--foreground)' }}>ðŸ”´ Live Chat</h1>
          <div className="flex items-center gap-2 mt-1">
            {user.photoURL && (
              <img 
                src={user.photoURL} 
                alt={user.name} 
                className="w-6 h-6 rounded-full"
              />
            )}
            <p className="text-sm" style={{ color: 'var(--text-muted)' }}>
              Welcome, {user.name}!
            </p>
          </div>
        </div>
        <ThemedButton onClick={logout} variant="outline" size="sm">
          Logout
        </ThemedButton>
      </div>

      {/* Messages */}
      <div className="flex-1 overflow-y-auto p-4 space-y-3">
        {messages.map((message) => (
          <div
            key={message.id}
            className={`flex ${message.userId === user.uid ? 'justify-end' : 'justify-start'}`}
          >
            <div className="max-w-xs lg:max-w-md">
              {message.userId !== user.uid && (
                <div className="flex items-center gap-2 mb-1">
                  {message.userPhoto && (
                    <img 
                      src={message.userPhoto} 
                      alt={message.username} 
                      className="w-4 h-4 rounded-full"
                    />
                  )}
                  <p className="text-xs" style={{ color: 'var(--text-muted)' }}>
                    {message.username}
                  </p>
                  {message.provider && (
                    <span className="text-xs px-1 rounded" style={{ 
                      background: message.provider === 'google.com' ? '#4285F4' : '#333',
                      color: 'white'
                    }}>
                      {message.provider === 'google.com' ? 'G' : 'GH'}
                    </span>
                  )}
                </div>
              )}
              <div
                className={`px-4 py-2 rounded-lg ${
                  message.userId === user.uid
                    ? 'theme-button'
                    : 'theme-card'
                }`}
                style={{
                  background: message.userId === user.uid ? 'var(--button-bg)' : 'var(--card-bg)',
                  color: message.userId === user.uid ? 'var(--button-text)' : 'var(--foreground)'
                }}
              >
                <p>{message.text}</p>
              </div>
            </div>
          </div>
        ))}
        <div ref={messagesEndRef} />
      </div>

      {/* Input */}
      <form onSubmit={sendMessage} className="p-4 theme-card">
        <div className="flex gap-2">
          <input
            type="text"
            value={newMessage}
            onChange={(e) => setNewMessage(e.target.value)}
            placeholder="Type your message..."
            className="flex-1 p-3 rounded-lg theme-input"
            style={{
              background: 'var(--input-bg)',
              border: '1px solid var(--input-border)',
              color: 'var(--foreground)'
            }}
            maxLength={500}
          />
          <ThemedButton type="submit" disabled={!newMessage.trim()}>
            Send
          </ThemedButton>
        </div>
      </form>
    </div>
  );
};

export default ChatApp;